#include "3-calc.h"

/**
 *op_add - return sum of a and b
 *@a: integer a;
 *@b: interger b
 *Return: a+b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 *op_sub - return difference of a and b
 *@a: integer variable
 *@b: integer variable
 *Return: a * b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *op_mul - product of a and b
 *@a: integer variable
 *@b: integer variable
 *Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 *op_div - divide a with b
 *@a: integer variable
 *@b: integer variable
 *Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 *op_mod - return the remainder of a in mod b
 *@a: integer variable
 *@b: integer variable
 *Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
